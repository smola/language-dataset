-- data setup
CREATE COLUMN TABLE "RESULTS" LIKE "T_RESULTS";
CREATE COLUMN TABLE "CENTERS" LIKE "T_CENTERS";
CREATE TYPE "T_ERRORS" AS TABLE ("CODE" INTEGER, "MESSAGE" VARCHAR(5000), "TIMESTAMP" TIMESTAMP);
CREATE COLUMN TABLE "ERRORS" LIKE "T_ERRORS";

-- exception proc setup
CREATE PROCEDURE "P_KMEDIANS_X" (IN data T_DATA, IN params T_PARAMS) LANGUAGE SQLSCRIPT AS
BEGIN
	DECLARE EXIT HANDLER FOR SQLEXCEPTION INSERT INTO "ERRORS" VALUES (::SQL_ERROR_CODE, ::SQL_ERROR_MESSAGE, CURRENT_TIMESTAMP);
	CALL "P_KMEDIANS" (:data, :params, results, centers) WITH OVERVIEW;
	-- subsequent processing will not be reached should an exception occur;
	SELECT COUNT(*) FROM :data;
	INSERT INTO "RESULTS" SELECT * FROM :results;
	INSERT INTO "CENTERS" SELECT * FROM :centers;
END;

